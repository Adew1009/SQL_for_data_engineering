select abs(-2.6);
-- The abs function returns the absolute value of a number (a positive number 2.6)

select ceil(2.7);
-- The ceil function returns the smallest integer greater than or equal to a number (3)

select floor(2.7);
-- The floor function returns the largest integer less than or equal to a number (2)    

select round(2.3456, 2);
-- The round function returns a number rounded to a specified number of decimal places (2.35)

select round(sqrt(4.0), 0);
-- The round function returns a number rounded to a specified number of decimal places (2)

select sqrt(4.0);
-- The sqrt function returns the square root of a number (2.00000000000000000000)

select power(2, 3);
-- The power function returns the result of raising a number to a specified power (8) 2x2x2


